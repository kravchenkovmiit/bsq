int			ft_atoi(char *str)
{
	long	i;
	long	res;
	int		neg;

	i = 0;
	res = 0;
	neg = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
			|| str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (neg || res > 2147483647)
		return (0);
	return ((int)res);
}
